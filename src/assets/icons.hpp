// Helper to draw 1-bit icon
inline void drawIcon(M5GFX* gfx, const uint8_t* icon, int16_t x, int16_t y, uint32_t color) {
    for (int row = 0; row < ICON_SIZE; row++) {
        for (int col = 0; col < ICON_SIZE; col++) {
            int byteIdx = row * 8 + col / 8;
            int bitIdx = 7 - (col % 8);
            if (icon[byteIdx] & (1 << bitIdx)) {
                gfx->drawPixel(x + col, y + row, color);
            }
        }
    }
}
